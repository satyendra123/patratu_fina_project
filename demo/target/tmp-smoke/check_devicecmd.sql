SET NOCOUNT ON;
SELECT TOP 20
  DC_ID,
  SLNO,
  CMD_DESC,
  ISNULL(CONVERT(INT, DC_RES), 0) AS DC_RES,
  ISNULL(CONVERT(INT, IS_DEL_EXECUTED), 0) AS IS_DEL_EXECUTED,
  CONVERT(VARCHAR(19), DC_DATE, 120) AS DC_DATE,
  CONVERT(VARCHAR(19), DC_cmd_date, 120) AS DC_cmd_date,
  LEFT(CAST(DC_CMD AS NVARCHAR(MAX)), 140) AS CMD_PREFIX
FROM DEVICECMD
WHERE CMD_DESC = 'JAVA:enableuser'
  AND CAST(DC_CMD AS NVARCHAR(MAX)) LIKE '%enrollid%1%'
ORDER BY DC_ID DESC;

SELECT
  COUNT(1) AS recent_disable_cmds
FROM DEVICECMD
WHERE CMD_DESC = 'JAVA:enableuser'
  AND CAST(DC_CMD AS NVARCHAR(MAX)) LIKE '%enrollid%1%'
  AND CAST(DC_CMD AS NVARCHAR(MAX)) LIKE '%enflag%0%'
  AND DC_DATE >= DATEADD(minute, -15, GETDATE());
